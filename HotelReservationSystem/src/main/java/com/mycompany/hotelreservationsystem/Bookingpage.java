    public Bookingpage() {
        initComponents();
         this.setLocationRelativeTo(null);
         
        try {
    Class.forName("com.mysql.cj.jdbc.Driver");
    Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/hotel reservation system", "root", "");

    // üîπ Fetch user details
    String userQuery = "SELECT username, email, phone_number, cnic FROM users WHERE user_id = ?";
    PreparedStatement userPst = con.prepareStatement(userQuery);
    userPst.setInt(1, LoggedInUser.userId);
    ResultSet userRs = userPst.executeQuery();

    if (userRs.next()) {
        jLabel33.setText(String.valueOf(LoggedInUser.userId));  // user_id
        jLabel17.setText(userRs.getString("username"));          // name
        jLabel18.setText(userRs.getString("email"));             // email
        jLabel19.setText(userRs.getString("cnic"));              // cnic
        jLabel20.setText(userRs.getString("phone_number"));      // phone
    }

    // üîπ Fetch latest booking of this user for selected room
    String bookingQuery = "SELECT * FROM bookings WHERE user_id = ? AND `room id` = ? ORDER BY booking_id DESC LIMIT 1";
    PreparedStatement bookingPst = con.prepareStatement(bookingQuery);
    bookingPst.setInt(1, LoggedInUser.userId);
    bookingPst.setString(2, Selectedroom.roomId);  // ‚úÖ Correct usage
    ResultSet bookingRs = bookingPst.executeQuery();

    String checkIn = "", checkOut = "";
    if (bookingRs.next()) {
        jLabel21.setText(bookingRs.getString("room id"));          // room id
        jLabel35.setText(bookingRs.getString("booking_id"));       // booking id
        checkIn = bookingRs.getString("check_in");
        checkOut = bookingRs.getString("check_out");
        jLabel22.setText(checkIn);                                 // check-in
        jLabel25.setText(checkOut);                                // check-out
        jLabel23.setText(bookingRs.getString("persons"));          // persons
    }

    // üîπ Calculate nights
    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
    Date checkInDate = sdf.parse(checkIn);
    Date checkOutDate = sdf.parse(checkOut);
    long diffMillis = checkOutDate.getTime() - checkInDate.getTime();
    int nights = (int) (diffMillis / (1000 * 60 * 60 * 24));
    jLabel26.setText(String.valueOf(nights));                     // nights

    
// üîπ Get room details (room type, price, persons) from room details table
String roomQuery = "SELECT `room type`, `Price`, `persons` FROM `room details` WHERE `room id` = ?";
PreparedStatement roomPst = con.prepareStatement(roomQuery);
roomPst.setString(1, Selectedroom.roomId);
ResultSet roomRs = roomPst.executeQuery();

if (roomRs.next()) {
    // ‚úÖ Set room type
    jLabel24.setText(roomRs.getString("room type"));

    // ‚úÖ Set price per night
    String pricePerNight = roomRs.getString("Price");
    jLabel27.setText(pricePerNight);

    // ‚úÖ Set persons from room table (not from booking)
    jLabel23.setText(roomRs.getString("persons"));

    // ‚úÖ Calculate total price
    double pricePerNightDouble = Double.parseDouble(pricePerNight);
    int total = (int) (pricePerNightDouble * nights);
    jLabel29.setText(total + " PKR");
}


    // üîπ Final statuses
  // Get today's date in yyyy-MM-dd format
java.util.Date today = new java.util.Date();
String currentDate = sdf.format(today);
jLabel28.setText(currentDate);

    jLabel30.setText("Confirmed");

    con.close();

} catch (Exception e) {
    javax.swing.JOptionPane.showMessageDialog(null, "‚ùå Receipt load error: " + e.getMessage());
}

        
        
    }
   


